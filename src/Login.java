    private void btnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccederActionPerformed
       
      PreparedStatement ps;
        ResultSet rs;
        
        String usuario = txtUsuario.getText();
        String pasword = String.valueOf(pswPasword.getPassword());
        
        String consulta = "Select * from registro where NombreUsuario =? and Pasword =?";
        
        try {
            ps = Conexion.getconnection().prepareStatement(consulta);
            
            ps.setString(1,usuario);
            ps.setString(2,pasword);
            
            rs = ps.executeQuery();
            
            if (rs.next()){
                
                JOptionPane.showMessageDialog(null,"Acceso al Sistema");
                
                opciones opc = new opciones();
                opc.setVisible(true);
                opc.pack(); // hace que el codigo se compacte para que sean visible todos los compoentes que esten dentro de el 
                opc.setLocationRelativeTo(null); //posiciona el frame en el centro de la pantalla al pasarle null como parametro
                dispose();
                
            }
            else if(usuario.equals("")){
                JOptionPane.showMessageDialog(null,"Favor ingrese el nombre de usuario");
            }
            else if(pasword.equals("")){
                JOptionPane.showMessageDialog(null,"Favor ingrese la Contrase√±a");
            }        
            else{
                JOptionPane.showMessageDialog(null,"Usuario o Contrase√±a son erroneos, sino esta registrado debe registrarse");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAccederActionPerformed
